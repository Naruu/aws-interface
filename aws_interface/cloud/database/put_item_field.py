
from cloud.response import Response
from cloud.util import has_write_permission, database_can_not_access_to_item

# Define the input output format of the function.
# This information is used when creating the *SDK*.
info = {
    'input_format': {
        'session_id': 'str',
        'item_id': 'str',
        'field_name': 'str',
        'field_value': '?',
    },
    'output_format': {
        'success': 'bool',
    }
}


def do(data, resource):
    body = {}
    params = data['params']
    user = data['user']

    item_id = params.get('item_id', None)
    field_name = params.get('field_name', None)
    field_value = params.get('field_value', None)

    item = resource.db_get_item(item_id)
    if database_can_not_access_to_item(item):
        body['success'] = False
        body['message'] = 'Database cannot access to system item'
        return Response(body)

    if has_write_permission(user, item):
        item[field_name] = field_value
        if field_value is None:
            item.pop(field_name)
        resource.db_update_item(item_id, item)
        body['success'] = True
    else:
        body['success'] = False
        body['message'] = 'permission denied'
    return Response(body)
